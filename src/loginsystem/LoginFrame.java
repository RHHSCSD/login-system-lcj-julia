    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        // TODO add your handling code here:
        lFrame.loadUser();
        
        String enteredN = enterName.getText();
        String enteredP = enterPass.getText();
        // Log the user in
        int loging = lFrame.loginSys(enteredN, enteredP);
        
        if (loging == 0) {
            loggedIn.setText("You logged in");
        } else if (loging == 1) {
            loggedIn.setText("Wrong password");
        } else if (loging == 2) {
            loggedIn.setText("Please register");
        }

    }//GEN-LAST:event_loginButtonActionPerformed

